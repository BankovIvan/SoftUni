INSERT INTO Passengers
(
	FullName
	, Email
)
	SELECT
		CONCAT(FirstName, ' ', LastName) AS FullName
		, CONCAT(TRIM(FirstName), TRIM(LastName), '@gmail.com') AS Email 
	FROM
		Pilots
	WHERE
		Id BETWEEN 5 AND 15













