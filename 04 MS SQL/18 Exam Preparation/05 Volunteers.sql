SELECT 
	[Name] AS [Name]
	, PhoneNumber AS [PhoneNumber]
	, [Address] AS [Address]
	, AnimalId AS [AnimalId]
	, DepartmentId AS [DepartmentId]
FROM
	Volunteers
ORDER BY
	[Name] ASC
	, AnimalID ASC
	, DepartmentId ASC
















