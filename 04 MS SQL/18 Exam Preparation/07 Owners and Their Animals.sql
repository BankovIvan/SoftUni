--USE Zoo;


SELECT TOP(5)
	o.[Name] AS [Owner] 
	, COUNT(*) AS [CountOfAnimals]
FROM
	Animals AS a
	JOIN Owners AS o
	ON a.OwnerId = o.Id
WHERE
	OwnerId IS NOT NULL
GROUP BY
	o.[Name]
ORDER BY
	COUNT(*) DESC
	, o.[Name] ASC













