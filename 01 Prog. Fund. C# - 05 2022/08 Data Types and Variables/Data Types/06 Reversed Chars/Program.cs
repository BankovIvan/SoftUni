using System;

namespace _06_Reversed_Chars
{
    class Program
    {
        static void Main(string[] args)
        {
            char a, b, c;

            a = char.Parse(Console.ReadLine());
            b = char.Parse(Console.ReadLine());
            c = char.Parse(Console.ReadLine());

            Console.WriteLine("{0} {1} {2}", c, b, a);

        }
    }
}
